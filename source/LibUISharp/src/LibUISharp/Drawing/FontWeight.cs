// uiTextWeight
namespace LibUISharp.Drawing
{
    /// <summary>
    /// Specifies the desity of a font, in terms of the lightness or heaviness of the strokes.
    /// </summary>
    public enum FontWeight : uint
    {
        /// <summary>
        /// Specifies the minimum font weight.
        /// </summary>
        Minimum = 0,

        /// <summary>
        /// Specifies the thin font weight.
        /// </summary>
        Thin = 100,

        /// <summary>
        /// Specifies the ultra-light font weight.
        /// </summary>
        UltraLight = 200,

        /// <summary>
        /// Specifies the light font weight.
        /// </summary>
        Light = 300,

        /// <summary>
        /// Specifies the book font weight.
        /// </summary>
        Book = 350,

        /// <summary>
        /// Specifies the normal font weight.
        /// </summary>
        Normal = 400,

        /// <summary>
        /// Specifies the medium font weight.
        /// </summary>
        Medium = 500,

        /// <summary>
        /// Specifies the semi-bold font weight.
        /// </summary>
        SemiBold = 600,

        /// <summary>
        /// Specifies the bold font weight.
        /// </summary>
        Bold = 700,

        /// <summary>
        /// Specifies the ultra-bold font weight.
        /// </summary>
        UltraBold = 800,

        /// <summary>
        /// Specifies the heavy font weight.
        /// </summary>
        Heavy = 900,

        /// <summary>
        /// Specifies the ultra-heavy font weight.
        /// </summary>
        UltraHeavy = 950,

        /// <summary>
        /// Specifies the maximum font weight.
        /// </summary>
        Maximum = 1000
    }
}